package com.kita.first.level1;

public class Operator4 {
	public static void main(String[] args) {
		int n = 7;
		n += 4;
		System.out.println(n);
		n -= 4;
		System.out.println(n);
		n *= 4;
		System.out.println(n);
		n /= 4;
		System.out.println(n);
		n %= 4;
		System.out.println(n);
		
	}
}
