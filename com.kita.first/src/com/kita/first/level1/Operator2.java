package com.kita.first.level1;

public class Operator2 {
	public static void main(String[] args) {
		int n1 = 3;
		System.out.println(n1++);
		System.out.println(n1);
		System.out.println(++n1);
		System.out.println(n1);
		System.out.println(n1--);
		System.out.println(n1);
		System.out.println(--n1);
		
	}
}
